# Divisão (/)
a = 10
b = 3
divisao = a / b # toda divisão serultará em um float
print(f"A divisão entre {a} e {b} é igual a {divisao}")

# Parte inteira da divisão (//)
a = 10
b = 3
parte_inteira_divisao = a // b # toda divisão serultará no inteiro
print(f"A parte inteira da divisão entre {a} e {b} é igual a {parte_inteira_divisao}")

# Resto da divisão
a = 10
b = 3
resto = a % b # toda divisão serultará no inteiro
print(f"O resto da divisão entre {a} e {b} é igual a {resto}")
